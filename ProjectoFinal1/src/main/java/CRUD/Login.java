    public void ValidarUsuario() {
        try {
            String Usuario = txtusuario.getText();
            String pass = String.valueOf(password.getPassword());
            String sql = "SELECT * FROM user WHERE usuario=? AND clave=?";
            
            PreparedStatement pst = conectar.prepareStatement(sql);
            pst.setString(1, Usuario);
            pst.setString(2, pass);
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                Veterinaria Ingreso = new Veterinaria();
                Ingreso.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Revise su usuario y contraseña");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la validación: " + e.getMessage());
        }
    }


